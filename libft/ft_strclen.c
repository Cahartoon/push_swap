#include "libft.h"

size_t	ft_strclen(const char *s, char c)
{
	size_t	i;

	if (s && c)
	{
		i = 0;
		while (*s != c && *s)
		{
			s++;
			i++;
		}
		return (i);
	}
	return (0);
}
